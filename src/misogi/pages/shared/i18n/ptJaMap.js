function hasJapanese(text) {
  return /[\u3040-\u30ff\u3400-\u9fff]/.test(text);
}

export function translateJaToPt(text) {
  const raw = String(text ?? '');
  if (!raw) return raw;
  if (!hasJapanese(raw)) return raw;
  if (JA_TO_PT_EXACT[raw]) return JA_TO_PT_EXACT[raw];
  let out = raw;
  for (const [ja, pt] of JA_TO_PT_TOKENS) {
    out = out.split(ja).join(pt);
  }
  return out;
}
